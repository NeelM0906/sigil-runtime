#!/usr/bin/env bash
###############################################################################
# SAI Migration Script — Mac Mini → Mac Studio
# Run this ON THE NEW MAC STUDIO as the target user.
#
# What it does:
#   1. Installs Homebrew, Node, pnpm, Python, OpenClaw
#   2. Clones the workspace from GitHub
#   3. Sets up all environment variables (API keys, Postgres, Supabase, etc.)
#   4. Installs the OpenClaw config (openclaw.json) with all 5 sisters
#   5. Creates sister workspaces
#   6. Sets up Python venvs for tools
#   7. Fixes security criticals (file perms, dmPolicy, groupPolicy)
#   8. Starts the gateway
#
# Usage:
#   chmod +x migrate-to-mac-studio.sh
#   ./migrate-to-mac-studio.sh
#
# Prerequisites:
#   - macOS with admin access
#   - Internet connection
#   - GitHub SSH key configured (or use HTTPS token)
###############################################################################

set -euo pipefail

RED='\033[0;31m'
GREEN='\033[0;32m'
YELLOW='\033[1;33m'
BLUE='\033[0;34m'
NC='\033[0m'

log()  { echo -e "${GREEN}[SAI]${NC} $1"; }
warn() { echo -e "${YELLOW}[WARN]${NC} $1"; }
err()  { echo -e "${RED}[ERROR]${NC} $1"; }
step() { echo -e "\n${BLUE}━━━ $1 ━━━${NC}"; }

OPENCLAW_DIR="$HOME/.openclaw"
WORKSPACE="$OPENCLAW_DIR/workspace"

