  brew install python3
else
  log "Python $(python3 --version) ✅"
fi

# Git
if ! command -v git &>/dev/null; then
  log "Installing git..."
  brew install git
else
  log "Git ✅"
fi

# ffmpeg (for voice, video, audio tools)
if ! command -v ffmpeg &>/dev/null; then
