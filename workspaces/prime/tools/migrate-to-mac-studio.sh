log "Workspace ready at $WORKSPACE ✅"

###############################################################################
# Step 5: Create Sister Workspaces
###############################################################################
step "Step 5/9: Sister Workspaces"

SISTERS=("forge" "scholar" "memory")

for sister in "${SISTERS[@]}"; do
  SISTER_DIR="$OPENCLAW_DIR/workspace-${sister}"
  mkdir -p "$SISTER_DIR"

  # Symlink shared tools directory so all sisters can access context_fetch etc.
  if [ ! -L "$SISTER_DIR/tools" ] && [ ! -d "$SISTER_DIR/tools" ]; then
    ln -s "$WORKSPACE/tools" "$SISTER_DIR/tools"
    log "Linked tools → workspace-${sister}/tools"
  fi

