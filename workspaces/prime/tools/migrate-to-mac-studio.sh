    log "Linked reports → workspace-${sister}/reports"
  fi

  # Symlink shared memory
  if [ ! -L "$SISTER_DIR/memory" ] && [ ! -d "$SISTER_DIR/memory" ]; then
    ln -s "$WORKSPACE/memory" "$SISTER_DIR/memory"
    log "Linked memory → workspace-${sister}/memory"
  fi

  log "workspace-${sister} ✅"
done

# Recovery workspace is inside main workspace (existing pattern)
mkdir -p "$WORKSPACE/sisters/sai-recovery"
log "Recovery workspace ✅"

###############################################################################
# Step 6: Python Virtual Environments
